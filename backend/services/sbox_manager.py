    STANDARD_NAMES = {"Standard SM4", "Standard AES", "Standard RC4", "Standard DES"}

    def __new__(cls):
        if cls._instance is None:
            cls._instance = super().__new__(cls)
            cls._instance._init()
        return cls._instance

    def _init(self):
        project_root = Path(__file__).resolve().parents[2]
        self.config_path = project_root / "data" / "sboxes.json"
        self.sboxes = self._load_sboxes()

    def _load_sboxes(self) -> Dict[str, List[int]]:
        sboxes = {
            "Standard SM4": self.STANDARD_SBOX,
            "Standard AES": self.STANDARD_AES_SBOX,
            "Standard RC4": self.STANDARD_RC4_SBOX,
            "Standard DES": self.STANDARD_DES_SBOX,
        }
        if self.config_path.exists():
            try:
                with self.config_path.open("r", encoding="utf-8") as file:
                    sboxes.update(json.load(file))
            except Exception as exc:
                print(f"Failed to load custom S-Boxes: {exc}")
        return sboxes

    def save_sboxes(self):
        custom_sboxes = {key: value for key, value in self.sboxes.items() if key not in self.STANDARD_NAMES}
        try:
            self.config_path.parent.mkdir(parents=True, exist_ok=True)
            with self.config_path.open("w", encoding="utf-8") as file:
                json.dump(custom_sboxes, file, ensure_ascii=False, indent=4)
        except Exception as exc:
            print(f"Failed to save custom S-Boxes: {exc}")

    def get_all_names(self) -> List[str]:
        return list(self.sboxes.keys())

    def get_sbox(self, name: str) -> List[int]:
        if name not in self.sboxes and name not in self.STANDARD_NAMES:
            self._init()
        return self.sboxes.get(name, self.STANDARD_SBOX)

    def add_sbox(self, name: str, sbox: List[int]) -> bool:
        if name in self.STANDARD_NAMES:
            return False
        self.sboxes[name] = sbox
        self.save_sboxes()
        return True

    def remove_sbox(self, name: str) -> bool:
        if name in self.STANDARD_NAMES:
            return False
        if name in self.sboxes:
            del self.sboxes[name]
            self.save_sboxes()
            return True
        return False
